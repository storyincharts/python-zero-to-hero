## Finding the Area of circle
#sample input: radius 5, Sample output:Area of the circle:78.53981633974483

r = int(input("Give any value:"))
Pi = 3.14
Area = Pi*r**2
print("Area of the circle:", Area) #or
print("Area of the circle:", Area,sep="") #or
print(f"Area of the circle:{Area}",sep="")