num1 = int(input("Give the value of num1:"))
num2 = int(input("Give the value of num2:"))

operator = input("Operator:")

if operator == "+":
    print(f"Addition:{num1+num2}")
elif operator== "-":
    print(f"Substraction:{num1-num2}")
elif operator== "*":
    print(f"Multiplication: {num1*num2}")
elif operator== "/":
    print(f"Division: {num1/num2}")
else:
    print("The calculator isn't equipped to do this operation")



















































































































